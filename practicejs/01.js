let age = 19;             // Number
let name = "Veer";        // String
let city = "someCity";    // String (instead of boolean true)
let account;              // Undefined (implicitly)
let wow = null;           // Null

console.log(typeof age);       // "number"
console.log(typeof name);      // "string"
console.log(typeof city);      // "string"
console.log(typeof account);   // "undefined"
console.log(typeof wow);       // "object" (null is considered an object in JavaScript)
